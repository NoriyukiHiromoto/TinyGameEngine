#include "Artemis.Local.hpp"

#include "Artemis.System.hpp"
#if defined(ATS_WINDOWS)
#include "Platform/Artemis.System.windows.cpp"
#endif//defined(ATS_WINDOWS)

//----------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------
namespace Artemis
{
namespace System
{

//----------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------
bool Initialize(const wchar_t ApplicationName[], SizeI ScreenSize, int32_t FrameRate)
{
	if (!Platform::Initialize(ApplicationName, ScreenSize, FrameRate))
	{
		return false;
	}

	return true;
}

//----------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------
void Uninitialize()
{
	Platform::Uninitialize();
}

//----------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------
bool Update()
{
	if (!Platform::Update())
	{
		return false;
	}

	return true;
}

//----------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------
void* GetWindowHandle()
{
	return Platform::GetWindowHandle();
}

//----------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------
PointI GetCursorPosition()
{
	return Platform::GetCursorPosition();
}

//----------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------
}
}
